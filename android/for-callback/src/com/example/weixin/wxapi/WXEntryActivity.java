	@Override
	
	public void onResp(BaseResp resp) {
		int result;
		
		switch (resp.errCode) {
		case BaseResp.ErrCode.ERR_OK:
			result = 1;
			break;
		case BaseResp.ErrCode.ERR_USER_CANCEL:
			result = 2;
			break;
		case BaseResp.ErrCode.ERR_AUTH_DENIED:
			result = 3;
			break;
		default:
			result = 4;
			break;
		}
		
		//接收到信息后，执行回调
		WeiXin.sendCallBack(result);
		finish();
		
	}
	
	
